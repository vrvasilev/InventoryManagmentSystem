    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int[] indexs = jTable1.getSelectedRows();

        Object[] row = new Object[3];
        DefaultTableModel defaultTable2Model = (DefaultTableModel) jTable2.getModel();
        Integer updateInteger;
        //Get text and parse 
        String text = jTextField2.getText();
        int quanty = Integer.parseInt(text);
        jTextField2.setText("");

        for (int i = 0; i < indexs.length; i++) {
            int indexToModel = jTable1.convertRowIndexToModel(indexs[i]);
            row[0] = defaultTableModel.getValueAt(indexToModel, 0);//name

            updateInteger = (int) defaultTableModel.getValueAt(indexToModel, 1) - quanty;
            updateData(row[0], updateInteger);
            row[1] = quanty;
            row[2] = quanty * (Float) defaultTableModel.getValueAt(indexToModel, 2);
            defaultTableModel.setValueAt(updateInteger, indexToModel, 1);
            defaultTable2Model.addRow(row);
            totalSum();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void totalSum() {
        float total = 0;
        for (int i = 0; i < jTable2.getRowCount(); i++) {
            float Amount = (float) jTable2.getValueAt(i, 2);
            total = Amount + total;

        }
        jLabel10.setText(total + "лв");
    }
