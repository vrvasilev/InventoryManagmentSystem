    void loadData() {
        String url = "jdbc:mysql://localhost:3306/management?autoReconnect=true&useSSL=false";
        String user = "root";
        String password = "root";
        String sql = "SELECT product_name, quantity , price FROM management.products";
        try {
            Connection con = (Connection) DriverManager.getConnection(url, user, password);
            Statement stm = (Statement) con.createStatement();
            ResultSet rs = stm.executeQuery(sql);

            int col = rs.getMetaData().getColumnCount();

            while (rs.next()) {
                Object[] row = new Object[col];
                for (int i = 1; i <= col; i++) {
                    row[i - 1] = rs.getObject(i);
                }
                defaultTableModel.insertRow(rs.getRow() - 1, row);
            }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

